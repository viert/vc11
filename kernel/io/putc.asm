;
;  putc.asm
;  pure assembler function displaying a character on the screen
;

	MODULE		putc
	INCLUDE		"z80_crt0.hdr"

; Constants

	DEFC 		KC_ENTER 	= $0D
	DEFC		KC_SPACE	= $20
	DEFC 		SIZE_X		= $20
	DEFC 		SIZE_Y		= $18
	DEFC 		_cursor_x	= $83F0
	DEFC 		_cursor_y	= $83F1

; Public scope functions and variables

	PUBLIC		_charset		; unsigned char[] 	charset
	PUBLIC		_cursor_x		; unsigned char* 	cursor_x
	PUBLIC		_cursor_y		; unsigned char* 	cursor_y
	PUBLIC		_putc			; void __FASTCALL__	putc(unsigned char code)

._putc
	ld 	a, l
	or 	a				; zero-terminated
	ret 	z				; 

	cp	KC_ENTER			; carrige return
	jr 	nz, putc_letters

	ld 	hl, _cursor_y
	ld 	a, (hl)
	inc 	a 
	cp 	SIZE_Y
	jr 	nz, putc_nowrap
	xor 	a
.putc_nowrap
	ld 	(hl), a
	ld 	hl, _cursor_x
	ld 	(hl), 0
	ret

.putc_letters
	cp 	KC_SPACE
	ret 	m
	ld 	c, a
	ld 	b, 0				; char code in BC
	sla 	c				; multiply BC by 8
	rl 	b
	sla 	c
	rl 	b
	sla 	c
	rl 	b
	ld	hl, _charset - (8 * KC_SPACE)
	add	hl, bc
	push 	hl
	call	get_cursor_screen_addr
	pop 	de				; HL - screen addr, DE - charset table addr
	ld 	b, 8				; copy 8 bytes
.lt_loop
	ld 	a, (de)
	ld	(hl), a
	inc 	de
	inc	h				; legendary 'next_line' subroutine from 90s
	ld	a, $07				; thoroughly restored by intuition
	and	h
	jr	nz, lt_next
	ld	a, $20
	add 	a, l
	ld 	l, a
	and 	$E0
	jr 	z, lt_next
	ld 	a, h
	sub 	8
	ld 	h, a
.lt_next
	dec 	b
	jr 	nz, lt_loop
						; copied!

	ld 	hl, _cursor_x			; incrementing x
	ld 	a, (hl)
	inc 	a
	and 	SIZE_X-1
	ld 	(hl), a
	ret 	nz				; x not wrapped, no need to modify y

	ld 	hl, _cursor_y
	ld 	a, (hl)
	inc 	a				; incrementing y
	cp 	SIZE_Y
	jr	nz, lt_y_nowrap
	xor 	a
.lt_y_nowrap
	ld 	(hl), a
	ret


.get_cursor_screen_addr
	ld 	hl, _cursor_y
	ld 	b, (hl)				; cursor_y in B
	ld 	a, $18
	and	b				; after masking, B has third of screen in bits 3,4
	add	a, $40				; high byte of screen start
	ld 	h, a				; high byte
	ld 	a, $07
	and	b
	sla 	a				; getting low byte
	sla 	a
	sla 	a
	sla 	a
	sla 	a
	ld 	l, a
	push 	hl
	ld 	hl, _cursor_x
	ld 	c, (hl)
	ld 	b, 0
	pop 	hl
	add 	hl, bc
	ret

